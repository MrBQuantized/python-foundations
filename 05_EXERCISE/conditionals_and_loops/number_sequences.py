# Random Numbers in Ascending Order 
# ======================================
"""
Problem:
Print numbers in ascending order from a number, A to limit, B.

Mathematical Principle:
The sequence of numbers from A to B is an 
arithmetic sequence with a common difference of d.

Pseudocode:
1. Initialize a counter to A.
2. Repeat the following steps until the counter reaches limit, B:
   a. Print the current value of the counter.
   b. Increase the counter by d.
"""

A = int(input("Enter the starting number: "))
B = int(input("Enter the limit number, B: "))
d = int(input("Enter the common difference, d: "))

for number in range(A, B + 1, d):
    print(number)



# ====================================== 
# Random Numbers in Descending Order
# ======================================
"""
Problem:
Print numbers in descending order of any given range (B to A).

Mathematical Principle:
The sequence of numbers from B to A is an 
arithmetic sequence with a common difference of d.

where d is negative.

Pseudocode:
1. Initialize a counter to B.
2. Repeat the following steps until the counter reaches A:
   a. Print the current value of the counter.
   b. Decrease the counter by d (d is negative).
"""
B = int(input("Enter the starting, B: "))
A = int(input("Enter the limit number, A: "))
d = int(input("Enter the common difference, d: "))


for number in range(B + 1, A, d):
    print(number)
    
