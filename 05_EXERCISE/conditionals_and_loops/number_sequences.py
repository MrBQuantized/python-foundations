# === Even Numbers ====
"""
Problem:
Print even numbers within a specified range (A -> B).

Mathematical Principle:
An even number is an integer that is exactly divisible by 2.
The sequence of even numbers can be generated by starting 
from the first even number and incrementing by 2.

Pseudocode:
1. Initialize a counter to 1.
2. Repeat the following steps until the counter reaches 100:
   a. Print the current value of the counter.
   b. Increment the counter by 1.
"""

for number in range(1, 101):
    if number % 2 == 0:
        print(number)
        

# Alternatively

for i in range(0, 101, 2):
    print(i)

    
# === Odd Numbers ====

for number in range(1, 51):
    if number % 2 != 0:
        print(number)
    
    # Alternatively

for i in range(1, 101, 2):
    print(i)
    
    
# ==== Combining both ===

for x in range(1, 51):
    if x % 2 == 0:
        print(f"{x} is even.") 
    else:
        print(f"{x} is odd.")
        
